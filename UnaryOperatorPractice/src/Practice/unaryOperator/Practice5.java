package Practice.unaryOperator;

public class Practice5 {

	public static void main(String[] args) {
		int i = 10;  
		i++;  
	
		System.out.println(i);  
		++i;                 
		
		System.out.println(i);  
	  
		System.out.println(++i);  
		
		System.out.println(i++);  
	 
		System.out.println(i);  

	}

}
