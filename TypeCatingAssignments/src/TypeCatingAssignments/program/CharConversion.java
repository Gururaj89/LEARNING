package TypeCatingAssignments.program;

public class CharConversion {

	public static void main(String[] args) {
		//character conversion in type casting
				char a=10;
				byte b;
			     b=(byte)a;	
				System.out.println(b);
				char c=10;
				short d;
				d=(short)c;
				System.out.println(d);
				char e=10;
				int f;
				f=e;
				System.out.println(f);
				char g=10;
				long h;
				h=g;
				System.out.println(h);
				char i=10;
				float j;
				j=i;
				System.out.println(j);
				//char k=10;
				//double l;
				//k=(double)l;   //double to char cannot convert
				//System.out.println(l);
	}

}
