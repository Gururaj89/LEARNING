package operators.logical.operators;

public class FirstProgram {

	public static void main(String[] args) {
	
		int a=20;
		int b=5;
		int c=25;
		
		System.out.println("A is equal to C ? " +(a==c));
		System.out.println("================================");
		System.out.println("A is equal to B ? " +(a==b));
		System.out.println("================================");
		System.out.println("B is equal to C ? " +(b==c));
		System.out.println("================================");
		System.out.println("A is not equal to B ? " +(a!=b));
		System.out.println("================================");
		System.out.println("B is not equal to C ? " +(b!=c));
		System.out.println("================================");
		System.out.println("A is Greater than B ? " +(a>b));
		System.out.println("================================");
		System.out.println("A is Greater than C ? " +(a>c));
		System.out.println("================================");
		System.out.println("B is Greater than A ? " +(b>a));
		System.out.println("================================");
		System.out.println("A is Lesser than B ? " +(a<b));
		System.out.println("================================");
		System.out.println("B is Lesser than A ? " +(b<a));
		System.out.println("================================");
		System.out.println("A is Greater than or equal to B ? " +(a>=b));
		System.out.println("================================");
		System.out.println("B is Greater than or equal to A ? " +(b>=a));
		System.out.println("================================");
		System.out.println("A is Greater than or equal to C ? " +(a>=c));
		System.out.println("================================");
	}

}
